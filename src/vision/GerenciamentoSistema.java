    public void setaCampos(){
        GerenciamentoSistemaControl sistema = new GerenciamentoSistemaControl(bancoOf);
        sistema.AlteraStatusAvaliacao();
        
        conecta.conexao();
        conecta.executaPesquisaSQL("Select * from gerenciamentosistemaV");
       try {
           conecta.rs.first();
           jTextFieldAvaliacoes.setText(conecta.rs.getString("StatusAvaliacaoSA"));
           jTextFieldSistema.setText(conecta.rs.getString("StatusSistemaSS"));
       } catch (SQLException ex) {
          JOptionPane.showMessageDialog(null,"Erro setaCampos."+ex);
       } finally {
           conecta.desconecta();
       }
    }
    
    public void geraComboSistema(){
        jComboBoxSistema.removeAllItems();
        conecta.conexao();
        conecta.executaPesquisaSQL("Select * from statussistema");
       try {
           conecta.rs.first();
           do{
             jComboBoxSistema.addItem(conecta.rs.getString("StatusSistemaSS"));
               
           }while (conecta.rs.next());
       } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,"Erro ao gerar combo Sistema. \n"+ex);
       } finally {
           conecta.desconecta();
       }
        
    }
    
     
