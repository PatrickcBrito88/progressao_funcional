
package valid_login;


public class FuncionarioModelValidacao {
    private int mat;
    private String nome;
    private int gerencia;
    private int diretoria;
    private int departamento;
    private int presidencia;
    private String cargo;
    private String senha;
    private String email;
   

    
    public int getMat() {
        return mat;
    }

    
    public void setMat(int mat) {
        this.mat = mat;
    }

   
    public String getNome() {
        return nome;
    }

    
    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getGerencia() {
        return gerencia;
    }

    public void setGerencia(int gerencia) {
        this.gerencia = gerencia;
    }

    public int getDiretoria() {
        return diretoria;
    }

    public void setDiretoria(int diretoria) {
        this.diretoria = diretoria;
    }

    public int getDepartamento() {
        return departamento;
    }

    public void setDepartamento(int departamento) {
        this.departamento = departamento;
    }

    public int getPresidencia() {
        return presidencia;
    }

    public void setPresidencia(int presidencia) {
        this.presidencia = presidencia;
    }

    public String getCargo() {
        return cargo;
    }

    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    
    
}
